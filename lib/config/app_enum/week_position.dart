/// Set week number position
///
/// WeekPosition.TOP = Set week number above the date picker
///
/// WeekPosition.LEFT = Set week number left side of the date picker
///
/// WeekPosition.RIGHT = Set week number right side of the date picker
///
/// WeekPosition.BOTTOM = Set week number below the date picker

enum WeekPosition {
  /// Set week number above the date picker
  TOP,
  /// Set week number left side of the date picker
  LEFT,
  /// Set week number right side of the date picker
  RIGHT,
  /// Set week number below the date picker
  BOTTOM
}
